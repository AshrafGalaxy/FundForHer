const parseDate = (dateString: string, status: string): Date => {
    if (!dateString) return addDays(new Date(), 365);
    
    const lowerDateString = dateString.toLowerCase();
    if (lowerDateString.includes('ended')) {
         // If it ended this year, make it upcoming for next year.
        const newDate = new Date();
        newDate.setFullYear(newDate.getFullYear() + 1);
        return newDate;
    }
    
    if (lowerDateString === 'open all year' || lowerDateString.includes('check annual dates') || lowerDateString.includes('rolling')) {
        return addDays(new Date(), 3650); // A date far in the future for always open
    }
    
    // Handle formats like 'dd-MMM-yyyy', 'MMM-yyyy', 'MMMM', 'dd-MM-yyyy', 'yyyy-MM-dd'
    const formats = ['dd-MMM-yyyy', 'MMM-yyyy', 'MMMM', 'dd-MM-yyyy', 'yyyy-MM-dd', 'MMMM d, yyyy'];
    for (const format of formats) {
        try {
            const parsed = parse(dateString, format, new Date());
            // Check if the parsed date is valid and not in the past. If in past, assume next year.
            if (!isNaN(parsed.getTime())) {
                const today = new Date();
                today.setHours(0, 0, 0, 0); // Reset time part for accurate comparison
                if (parsed < today) {
                    parsed.setFullYear(parsed.getFullYear() + 1);
                }
                return parsed;
            }
        } catch (e) { /* continue to next format */ }
    }

    // Handle month-only strings like 'April-May' -> use the end month
    try {
        const month = dateString.split('-')[1] || dateString.split('-')[0];
        const parsed = parse(month, 'MMMM', new Date());
         if (!isNaN(parsed.getTime())) {
            const today = new Date();
            today.setHours(0, 0, 0, 0);
            if (parsed < today) {
                parsed.setFullYear(parsed.getFullYear() + 1);
            }
            return parsed;
        }
    } catch (e) { /* continue */ }

    // Final fallback: if no format matches, set a default far-future date.
    return addDays(new Date(), 365);
};


